/**
 *  @brief  md5 NSData
 *
 *  @param key 密钥
 *
 *  @return 结果
 */
- (NSData *)tx_hmacMD5DataWithKey:(NSData *)key {
    return [self tx_hmacDataUsingAlg:kCCHmacAlgMD5 withKey:key];
}
